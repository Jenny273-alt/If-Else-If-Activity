let score = prompt("Enter your score:");

// Convert the score to a number
score = Number(score);

let grade;

if (score >= 90) {
  grade = "Excellent";
} else if (score >= 80) {
  grade = "Good";
} else if (score >= 70) {
  grade = "Fair";
} else {
  grade = "Needs Improvement";
}

// Print the grade
console.log("Your grade is:", grade);

